package com.example.pojo;

public class Usuarios {
	
	private String usuarioId;
	private String nombre;
	private String apellidoPateno;
	private String apellidoMaterno;
	private String correoElectronico;
	private String contrasena;
	private String pagado;
	private String usuarioReferencia;
	
	public String getUsuarioId() {
		return usuarioId;
	}
	public void setUsuarioId(String usuarioId) {
		this.usuarioId = usuarioId;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApellidoPateno() {
		return apellidoPateno;
	}
	public void setApellidoPateno(String apellidoPateno) {
		this.apellidoPateno = apellidoPateno;
	}
	public String getApellidoMaterno() {
		return apellidoMaterno;
	}
	public void setApellidoMaterno(String apellidoMaterno) {
		this.apellidoMaterno = apellidoMaterno;
	}
	public String getCorreoElectronico() {
		return correoElectronico;
	}
	public void setCorreoElectronico(String correoElectronico) {
		this.correoElectronico = correoElectronico;
	}
	public String getContrasena() {
		return contrasena;
	}
	public void setContrasena(String contrasena) {
		this.contrasena = contrasena;
	}
	public String getPagado() {
		return pagado;
	}
	public void setPagado(String pagado) {
		this.pagado = pagado;
	}
	public String getUsuarioReferencia() {
		return usuarioReferencia;
	}
	public void setUsuarioReferencia(String usuarioReferencia) {
		this.usuarioReferencia = usuarioReferencia;
	}

}
